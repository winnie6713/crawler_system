package wuit.application;

/**
 *
 * @author lxl
 */
public class Module{
   public int id = -1;
   public int pid = -1;
   public String name = "";
   public String remark = "";
   public String mode = "";
   public String param = "";
   
   @Override
   public String toString(){
      return name;
   }
   
   public int getPathCount(){
       return Integer.parseInt(param.substring(0,1));
   }
   
   public int getFileCount(){
       return Integer.parseInt(param.substring(1,2));
   }
   
   public int getFiledCount(){
       return Integer.parseInt(param.substring(2,3));
   }
   
   public int getKeyCount(){
       return Integer.parseInt(param.substring(3,4));
   }
}
